	PROCESS (clk)
		VARIABLE bch_parity : STD_LOGIC_VECTOR(15 DOWNTO 0);
		VARIABLE even_parity : STD_LOGIC;
		VARIABLE xor_temp : STD_LOGIC;
	BEGIN
		IF RISING_EDGE(clk) THEN
			IF rst = '1' THEN
				code_out <= (OTHERS => '0');
				code_valid <= '0';
				output_reg <= '0';
			ELSE

				-- Step 4: registered output send with valid pulse
				IF (output_reg = '1') THEN
					code_out <= data_reg & parity_reg & even_parity_reg;
				ELSE 
					code_out <= (OTHERS => '0');
				END IF;


