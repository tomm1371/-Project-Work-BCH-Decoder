						even_parity := even_parity XOR data_in(i);
					END LOOP;
					FOR i IN 0 TO 15 LOOP
						even_parity := even_parity XOR bch_parity(i);
					END LOOP;

					-- Step 3: register result 
					parity_reg <= bch_parity;
					even_parity_reg <= even_parity;
				END IF;
